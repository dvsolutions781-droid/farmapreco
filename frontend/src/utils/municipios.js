export function buscarMunicipios(texto) {
  if (!texto.trim()) return MUNICIPIOS;
  const normalizado = texto.normalize('NFD').replace(/[̀-ͯ]/g, '').toUpperCase();
  return MUNICIPIOS.filter(m =>
    m.nome.normalize('NFD').replace(/[̀-ͯ]/g, '').includes(normalizado)
  );
}
